import { checkIfExist } from './check-if-n-and-its-double-exist';

describe('check if n and its double exist', () => {
  test.each([
    { array: [0,0] },
    { array: [10,2,5,3] },
    { array: [7,1,14,11] },
    { array: [80,40,20,10] },
    { array: [1,2,3,4,5,6,7,8] },
    { array: [-10,12,-20,-8,15] },
    { array: [-20,8,-6,-14,0,-19,14,4] },
    { array: [10,20,40,80,160,11,4982,2349] },
    { array: [93,-164,862,-759,352,-395,-432,-492,-225,535,145,-992,965,-612,82,-102,185,-957,-937,285,992,240,-504,710,-467,-577,-646,-417,-697,110,-590,-441,-363,199,551,-487,704,399,-86,620,763,656,751,993,-656,116,-269,5,-140,752,-751,575,-573,168,800,-216,-581,-419,660,-779,851,653,-951,-759,165,530,-973,116,-393,976,380,558,483,993,329,-721,-847,24,-235,172,649,-363,-105,30,387,-270] },
  ])('truthy cases', ({ array }) => {
    expect(checkIfExist(array)).toBeTruthy();
  });

  test.each([
    { array: [3,1,7,11] },
    { array: [21,3,7,19,35,101] },
    { array: [-2,0,10,-19,4,6,-8] },
  ])('falsy cases', ({ array }) => {
    expect(checkIfExist(array)).toBeFalsy();
  });
});


